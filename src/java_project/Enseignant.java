    public void fill_box()
    {
        Connection con = Java_project.Dbconnection();
        try {
            PreparedStatement ps;
            ps = con.prepareStatement("SELECT DISTINCT libelle FROM correspondance JOIN matiere ON correspondance.id_mat = matiere.id_mat AND id_enseignant = ?");
            ps.setString(1, ID);
            ResultSet rs = ps.executeQuery();
            while (rs.next())
            {
                Matiere.addItem(rs.getString(1));
            }
        }catch (Exception e)
        {
            System.out.println(e.getMessage());
        }
        finally
        {
            try {
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Enseignant.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

