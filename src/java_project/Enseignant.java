    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        Connection con = Java_project.Dbconnection();
        try{
        String id=id_etudiant.getText();
        int numseance=Integer.parseInt(Ns.getText());
        String date=Date.getText();
        String matiere=Matiere.getSelectedItem().toString();
        PreparedStatement ps = con.prepareStatement("SELECT id_mat FROM matiere WHERE libelle=?");
        ps.setString(1,matiere);
        ResultSet rs=ps.executeQuery();
        rs.next();
        int id_mat=rs.getInt(1);
        ps=con.prepareStatement("INSERT INTO absence VALUES(?,?,?,?,?)");
        ps.setString(1, id);
        ps.setString(2, ID);
        ps.setInt(3, id_mat);
        ps.setInt(4, numseance);
        ps.setString(5, date);
        ps.executeUpdate();
        JOptionPane.showMessageDialog(rootPane,"Succes de l'operation");
        System.out.println("Succes de l'operation");
        id_etudiant.setText("");
        Ns.setText("");
        Date.setText("");
        }catch( SQLException|NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane,"Error!");
            System.out.println("Succes de l'operation");
            System.out.println(e.getMessage());
            
        }
        finally
        {
            try {
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Enseignant.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jLabel7MouseClicked

